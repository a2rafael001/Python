def is_intersect(shape1, shape2):
    """–ü—Ä–æ—Å—Ç–µ–π—à–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞ –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏—è –¥–≤—É—Ö –æ–±—ä–µ–∫—Ç–æ–≤"""
    if not isinstance(shape1, Shape) or not isinstance(shape2, Shape):
        raise TypeError("–û–±–∞ –æ–±—ä–µ–∫—Ç–∞ –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å —ç–∫–∑–µ–º–ø–ª—è—Ä–∞–º–∏ –∫–ª–∞—Å—Å–∞ Shape –∏–ª–∏ –µ–≥–æ –Ω–∞—Å–ª–µ–¥–Ω–∏–∫–æ–≤.")
    
    dist_x = abs(shape1.position[0] - shape2.position[0])
    dist_y = abs(shape1.position[1] - shape2.position[1])
    max_distance = (shape1.side_length + shape2.side_length) / 2
    if dist_x <= max_distance and dist_y <= max_distance:
        print(f"{shape1.identifier} –∏ {shape2.identifier} –ø–µ—Ä–µ—Å–µ–∫–∞—é—Ç—Å—è.")
        return True
    else:
        print(f"{shape1.identifier} –∏ {shape2.identifier} –Ω–µ –ø–µ—Ä–µ—Å–µ–∫–∞—é—Ç—Å—è.")
        return False

